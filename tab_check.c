#include <push_swap.h>

void	send_to_tab(int *tab)
{
	int i;
	int j;

	if (!tab)
		return (NULL);
	i = 1;
	j = 0;
	while (tab[i])
	{
		tab[j] = tab[i];
		i++;
		j++;
	}
}